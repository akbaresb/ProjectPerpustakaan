    private void cTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cTransaksiActionPerformed
                                                 
    if (cTransaksi.getSelectedItem() == null) {
        return; 
    }
    String noTransaksi = cTransaksi.getSelectedItem().toString();

    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("No Transaksi");
    model.addColumn("Nama");
    model.addColumn("Buku"); // Menampilkan Judul Buku
    model.addColumn("Tanggal Pinjam");
    model.addColumn("Tanggal Kembali");

    try {
        java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();

        String sql = "SELECT p.no_transaksi, a.nama, b.judul, p.tgl_pinjam, p.tgl_kembali " +
                     "FROM peminjaman p " +
                     "JOIN anggota a ON p.id_member = a.id_member " + 
                     "JOIN buku b ON p.id_buku = b.id_buku " +
                     "WHERE p.no_transaksi = ?";
                     
        java.sql.PreparedStatement pstkembali = conn.prepareStatement(sql);
        pstkembali.setString(1, noTransaksi);
        
        java.sql.ResultSet rs = pstkembali.executeQuery();

        // 5. Masukkan data ke dalam baris tabel
        if (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("no_transaksi"),
                rs.getString("nama"),
                rs.getString("judul"),
                rs.getString("tgl_pinjam"),
                rs.getString("tgl_kembali")
            });
            
            // 6. Otomatis mengisi textfield Tanggal Tenggat
            // Pastikan variabel textfield tanggal tenggat Anda bernama tTenggat
            tglTenggat.setText(rs.getString("tgl_kembali")); 
        }

        // 7. Terapkan model ke JTable di form Anda
        // Ganti 'tblPengembalian' dengan nama variabel JTable Anda yang sebenarnya
        tblPinjam.setModel(model);

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error memuat detail transaksi: " + e.getMessage());
    }     // TODO add your handling code here:
    }//GEN-LAST:event_cTransaksiActionPerformed

