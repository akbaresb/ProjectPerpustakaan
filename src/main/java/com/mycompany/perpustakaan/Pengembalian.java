    private void tIdAnggotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tIdAnggotaActionPerformed
     
    try {
        java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();
        cTransaksi.removeAllItems();
        String idAnggota = tIdAnggota.getText();
        String sqlA = "SELECT * FROM anggota WHERE id_member = ?";
        java.sql.PreparedStatement pstanggota = conn.prepareStatement(sqlA);
        pstanggota.setString(1, idAnggota);
        java.sql.ResultSet rs = pstanggota.executeQuery();
         if (rs.next()) {
            tNama.setText(rs.getString("nama"));
            
          
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Anggota tidak ditemukan!");
            tNama.setText("");
            tIdAnggota.requestFocus();
         }
        pstanggota.setString(1, idAnggota);
        if (idAnggota.trim().isEmpty()) {
            return;
        }
        
        
        String sql = "SELECT no_transaksi, id_buku FROM peminjaman WHERE id_member = ? AND status = 'Pinjam'";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, idAnggota);
        
        java.sql.ResultSet rs1 = pst.executeQuery();
        boolean adaData = false;
        
        // 3. Masukkan setiap nomor peminjaman yang ditemukan ke dalam ComboBox
        while (rs1.next()) {
            adaData = true;
            cTransaksi.addItem(rs1.getString("no_transaksi"));
        }
        if (!adaData) {
            javax.swing.JOptionPane.showMessageDialog(this, "Tidak ada transaksi peminjaman aktif untuk Anggota ini!");
            tIdAnggota.requestFocus();
        }
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Terjadi Kesalahan: " + e.getMessage());
    }       // TODO add your handling code here:
    }//GEN-LAST:event_tIdAnggotaActionPerformed

