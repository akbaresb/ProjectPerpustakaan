        String passTerinkripsi = encryptPassword(new String(password.getPassword()));
        String sql = "INSERT INTO petugas VALUES (?, ?, ?, ?, ?)";
        
        java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();
